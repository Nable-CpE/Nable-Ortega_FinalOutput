    private void task_update_button_1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_task_update_button_1ActionPerformed
        DefaultTableModel table = (DefaultTableModel)this.database.getModel();
        Object clm_Value = table.getValueAt(database.getSelectedRow(), 1);
        
        if(database.getSelectedRowCount()==1){
            if(clm_Value.equals("Pending")){
                count_pending--;
                pending_counter.setText(""+count_pending);
                pending_counter_delete.setText(""+count_pending);
                
            }else{
                count_completed--;
                completed_counter.setText(""+count_completed);
                completed_counter_delete.setText(""+count_completed);
            }
            table.removeRow(database.getSelectedRow());
            task_edit_name.setText("");
            task_edit_deadline.setText("");
            JOptionPane.showMessageDialog(this, "Successfully Deleted");
        }else if(database.getRowCount() ==0){
                JOptionPane.showMessageDialog(this, "Database is Empty!");
        }else{
                JOptionPane.showMessageDialog(this, "Please Select Row!");
        }
    }//GEN-LAST:event_task_update_button_1ActionPerformed

    private void task_update_button_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_task_update_button_ActionPerformed
        DefaultTableModel update = (DefaultTableModel)database.getModel();
        Object clm_Value = update.getValueAt(database.getSelectedRow(), 1);
        
        if(database.getSelectedRowCount()==1){
            String task_name = task_edit_name.getText();
            String task_status = (String) task_edit_status.getSelectedItem();
            String task_deadline = task_edit_deadline.getText();
            
            if(clm_Value.equals("Completed") && task_status.equals("Pending")){
                count_pending++;
                count_completed--;
                pending_counter.setText(""+count_pending);
                pending_counter_delete.setText(""+count_pending);
                completed_counter.setText(""+count_completed);
                completed_counter_delete.setText(""+count_completed);
            }else if(clm_Value.equals("Pending") && task_status.equals("Completed")){
                count_pending--;
                count_completed++;
                completed_counter.setText(""+count_completed);
                completed_counter_delete.setText(""+count_completed);
                pending_counter.setText(""+count_pending);
                pending_counter_delete.setText(""+count_pending);
            }

            
            update.setValueAt(task_name, database.getSelectedRow(), 0);
            update.setValueAt(task_status, database.getSelectedRow(), 1);
            update.setValueAt(task_deadline, database.getSelectedRow(), 2);
            
            JOptionPane.showMessageDialog(this, "Updated Successfully!");
            task_edit_name.setText("");
            task_edit_deadline.setText("");
        } else if (database.getSelectedRowCount() == 0){
            JOptionPane.showMessageDialog(this, "Database is Empty!");
        } else{
            JOptionPane.showMessageDialog(this, "Please Select Row To Update");
        }
    }//GEN-LAST:event_task_update_button_ActionPerformed

