    private void add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_buttonActionPerformed
        if(new_task.getText().equals("") || new_task_deadline.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please fill up all the requirments");
        } else{
            String task_name = new_task.getText();
            String task_status = (String) new_task_status.getSelectedItem();
            String task_deadline = new_task_deadline.getText();
        
        
            if(new_task_status.getSelectedItem().equals("Pending")){
                count_pending++;
                pending_counter.setText(""+count_pending);
                pending_counter_delete.setText(""+count_pending);
            } else{
                count_completed++;
                completed_counter.setText(""+count_completed);
                completed_counter_delete.setText(""+count_completed);
            }
        
            Object rowData[] = new Object[3];
            rowData[0] = task_name;
            rowData[1] = task_status;
            rowData[2] = task_deadline;
            DefaultTableModel edit_table = (DefaultTableModel)this.database.getModel();
            edit_table.addRow(rowData);
            JOptionPane.showMessageDialog(this, "Added Successfully!");
            new_task.setText("");
            new_task_deadline.setText("October 14, 2023");
        }
        
        
    }//GEN-LAST:event_add_buttonActionPerformed

